package main

import (
	"strings"
)

// AnsiFont is a named bitmap font used by text insert.
type AnsiFont struct {
	Name   string
	Width  int // glyph cell width
	Height int // glyph rows
	Glyphs map[rune][]string
}

// BuiltinFonts are the selectable ANSI-style fonts.
var BuiltinFonts = []*AnsiFont{
	fontBlock,
	fontOutline,
	fontWide,
	fontMini,
}

func fontByIndex(i int) *AnsiFont {
	if i < 0 || i >= len(BuiltinFonts) {
		return BuiltinFonts[0]
	}
	return BuiltinFonts[i]
}

func listFontNames() []string {
	out := make([]string, len(BuiltinFonts))
	for i, f := range BuiltinFonts {
		out[i] = f.Name
	}
	return out
}

// RenderText builds a glyph grid (row-major lines) for text using font and size.
// size scales each glyph cell by nearest-neighbor (1–4).
func (f *AnsiFont) RenderText(text string, size int) [][]rune {
	if size < 1 {
		size = 1
	}
	if size > 4 {
		size = 4
	}
	text = strings.ToUpper(text)
	if text == "" {
		return nil
	}

	baseH := f.Height
	gap := 1
	totalW := 0
	glyphs := make([][]string, 0, len(text))
	for _, ch := range text {
		g, ok := f.Glyphs[ch]
		if !ok {
			g = f.blankGlyph()
		}
		glyphs = append(glyphs, g)
		totalW += f.Width*size + gap*size
	}
	if totalW > 0 {
		totalW -= gap * size
	}
	totalH := baseH * size
	grid := make([][]rune, totalH)
	for y := 0; y < totalH; y++ {
		grid[y] = make([]rune, totalW)
		for x := range grid[y] {
			grid[y][x] = ' '
		}
	}

	xOff := 0
	for _, g := range glyphs {
		for row := 0; row < baseH && row < len(g); row++ {
			line := []rune(padGlyphRow(g[row], f.Width))
			for col := 0; col < f.Width && col < len(line); col++ {
				ch := line[col]
				if ch == ' ' {
					continue
				}
				for dy := 0; dy < size; dy++ {
					for dx := 0; dx < size; dx++ {
						y := row*size + dy
						x := xOff + col*size + dx
						if y < totalH && x < totalW {
							grid[y][x] = scaleChar(ch, size)
						}
					}
				}
			}
		}
		xOff += f.Width*size + gap*size
	}
	return grid
}

func scaleChar(ch rune, size int) rune {
	if size <= 1 {
		return ch
	}
	// Prefer solid block when scaling half-blocks for cleaner large text.
	switch ch {
	case '▀', '▄', '▌', '▐', '░', '▒', '▓':
		return '█'
	}
	return ch
}

func padGlyphRow(s string, w int) string {
	r := []rune(s)
	if len(r) >= w {
		return string(r[:w])
	}
	return string(r) + strings.Repeat(" ", w-len(r))
}

func (f *AnsiFont) blankGlyph() []string {
	rows := make([]string, f.Height)
	for i := range rows {
		rows[i] = strings.Repeat(" ", f.Width)
	}
	return rows
}

